public class SameIndex {
    public int index1;
    public int index2;
    public int finalIndex;
    public SameIndex(int index1,int index2,int finalIndex)
    {

        this.index1=index1;
        this.index2=index2;
        this.finalIndex=finalIndex;
    }
    public int getIndex1(){return index1;}
    public int getIndex2(){return index2;}
    public int getFinalIndex(){return finalIndex;}
}
